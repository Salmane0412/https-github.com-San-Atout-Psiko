<section id="Frequence_cardiaque">
    <article id="introduction">
        <h3>Fréquence cardiaque : </h3>
        <p>Ce test consiste à mesurer le nombre de battements cardiaques par une unité de temps et que
            chaque changement de ces pulsations soit au repos ou au cours d’un activité physique peut indiquer
            l’existence d’un certain niveau de stress car certaines personnes ont peut-être peur de l’avion et le
            test de stress en mesurant la fréquence cardiaque permet de savoir si le pilote est à l’aise avec son
            environnement (dépression barométrique, urgences médicales en avion).</p>
    </article>

</section>
<section id="Temperature_peau">
    <article id="introduction">
        <h3>Température de la peau :</h3>
        <p>Ce test a pour but de mesurer la température superficielle de la peau. C’est une méthode qui va
            nous permettre de connaitre l’état physique et psychique d'un utilisateur, c'est à dire : une
            température trop élevée ça peut indiquer soit l’existence d'une maladie soit un niveau de stress
            très élevée. Pour effectuer ce test il suffit juste de mettre son doigt pendant une durée moyenne sur
            le capteur de température pour avoir une valeur exacte.</p>
    </article>

</section>
<section id="Reflexes_visuels">
    <article id="introduction">
        <h3>Réflexes visuels :</h3>
        <p> Ce test consiste à tester la capacité d’un conducteur (l’avion, voiture) à réagir avec son
            environnement de façon rapide et de concentrer sur son travail malgré la présence des perturbations
            extérieurs. Pour ce faire l’utilisateur va concentrer sur une led et sur ses deux cotes on va mettre des
            leds multicolores qui s’allument de façon aléatoire et si l’un des leds de cotés allume il faut appuyer
            sur un bouton poussoir et ainsi de suite pour avoir ; à la fin ; un score.</p>
    </article>

</section>
<section id="Reconnaissance_tonalite">
    <article id="introduction">
        <h3>Reconnaissance de tonalité :</h3>
        <p>Ce test consiste à mesurer la capacité d’un conducteur à reconstruire un son après l’avoir écouté.
            Pour garantie l’interaction totale avec l’environnement. Pour ce faire L’utilisateur écoute avec un
            casque qui génère un son et puis l’utilisateur va essayer de reconstruire le son en chantant proche
            d’un micro.</p>
    </article>

</section>
<section id="Memorisation_couleurs">
    <article id="introduction">
        <h3>Mémorisation de couleurs :</h3>
        <p>Tester la capacité d’un conducteur à retenir les différents évènements se produisant autour de lui et
            mémériser plusieurs tâches au cours de conduire. Pour faire ce test l’utilisateur va essayer de
            concentrer sur une suite colorée de leds qui s’allument de façon aléatoire puis appuyer sur les
            boutons correspondants à chaque couleur et s’il arrive à refaire pareil on va lui attribuer un score de
            test.</p>
    </article>

</section>