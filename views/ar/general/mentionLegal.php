        <h2><strong> المادة 6</strong> :الكوكيز</h2>
        <p>يتم إبلاغ المستخدم أنه أثناء زياراته للموقع ، يمكن تثبيت ملف تعريف ارتباط تلقائيًا على برنامج المتصفح الخاص به. ملفات تعريف الارتباط هي ملفات صغيرة يتم تخزينها مؤقتًا على القرص الصلب لجهاز الكمبيوتر الخاص بالمستخدم بواسطة المتصفح الخاص بك وهي ضرورية لاستخدام موقع Ψ-Co. </br>
            لا تحتوي ملفات تعريف الارتباط على معلومات شخصية ولا يمكن استخدامها للتعرف على شخص ما. </br>
            يحتوي ملف تعريف الارتباط على معرف فريد ، يتم إنشاؤه بشكل عشوائي ومن ثم مجهول. تنتهي صلاحية بعض ملفات تعريف الارتباط في نهاية زيارة المستخدم ، وتبقى ملفات تعريف الارتباط الأخرى. </br>
            يتم استخدام المعلومات الموجودة في ملفات تعريف الارتباط لتحسين الموقع ، على سبيل المثال من خلال:
            • تذكر أنك أنت بالفعل أعطى المستخدم اسم المستخدم وكلمة المرور الخاصة به ، بحيث لا تضطر إلى تكرارها في كل صفحة جديدة. </br>
            • مراقبة كيفية استخدام المستخدمين للخدمات ، لتسهيل استخدامها وتخصيص طاقة كافية لضمان تجاوبهم. </br>
            • تحليل البيانات "المجهولة الهوية" للمساعدة في فهم كيفية تفاعل المستخدمين مع مختلف جوانب الخدمات عبر الإنترنت ، وبالتالي السماح بتحسينها. </br>
            من خلال تصفح الموقع ، يقبلها المستخدم. </br>
            في حالة عدم القبول ، يتم إبلاغ المستخدم بأنه قد يتم رفض بعض الوظائف أو الصفحات. يمكن للمستخدم إلغاء تنشيط ملف تعريف الارتباط هذا من خلال المعلمات التي تظهر في برنامج المتصفح الخاص به.
        </p></br>
