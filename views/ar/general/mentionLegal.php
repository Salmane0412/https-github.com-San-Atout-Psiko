        <h2><strong> المادة 3</strong> :البياتات الشخصيى</h2>
        <p>تتعهد Ψ-Co بالامتثال للوائح المعمول بها والمطبقة على معالجة البيانات الشخصية ، ولا سيما اللائحة (الاتحاد الأوروبي) 2016/679 للبرلمان الأوروبي والمجلس بتاريخ 27 أبريل 2016 المعمول بها اعتبارًا من 25 مايو 2018 .
            يوفر الموقع للمستخدم جمع ومعالجة المعلومات الشخصية فيما يتعلق بالخصوصية وفقًا للقانون رقم 78-17 المؤرخ 6 يناير 1978 فيما يتعلق بالبيانات والملفات والحريات. أعلن موقعنا ل CNIL.
            تتوافق هذه البيانات مع البيانات المقدمة عند التسجيل في الموقع وعند إجراء الاختبارات: فهي تتعلق بالاسم والاسم الأول ورقم الهاتف والعنوان والبريد الإلكتروني والجنس والنتائج بالإضافة إلى تاريخ الاختبار للشخص المعني.
            بموجب قانون حماية البيانات المؤرخ 6 يناير 1978 ، يحق للمستخدم الوصول إلى بياناته الشخصية وتصحيحها وحذفها ومعارضتها. يمارس المستخدم هذا الحق:
            • بالبريد الإلكتروني إلى psi-co@isep.fr
            • عن طريق البريد: ISEP ، شارع دي فانفيس ، إيسي ليه مولينو </p></br>
    </article>
