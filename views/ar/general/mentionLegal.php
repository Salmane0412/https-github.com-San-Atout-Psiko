        <h2><strong>المادة 2</strong>: الولوج للموقع الالكتروني</h2>
        <p>:موقع بسي-كو يضمن للمستخدم ولوجا مجانيا </br>
                  التسجيل - </br>
            خوض الإختبارات- </br>
            تتبع النتائج - </br>
  التواصل مع مدراء الموقع - </br>
  الموقع متاح مجانا في كل مكان ولكل مستخدم لديه حق الولوج للأنترنت. ويتكلف المستخدم بتكلفة الولوج للخدمات من المعدات المعلوماتية و التطبيقات و الولوج للأنترنت و و و  </br>
            
لا يمتلك المستخدم غير العضو حق الوصول إلى الخدمات المحجوزة. لذلك ، يجب عليه التسجيل عن طريق ملء إستمارة التسجيل. من خلال الموافقة على الاشتراك في الخدمات المحجوزة ، يتعهد المستخدم العضو بتقديم معلومات صادقة و بالضبط فيما يتعلق بوضعه المدني وتفاصيل الاتصال ، بما في ذلك عنوان بريده الإلكتروني. المستخدم هو المسؤول عن تحديث المعلومات المقدمة. وله الحق في تعديلها عن طريق تسجيل الدخول إلى منطقة الأعضاء الخاصة به. للوصول إلى الخدمات ، يجب على المستخدم تعريف نفسه باستخدام اسم المستخدم وكلمة المرور الخاصة به  سيتم إبلاغه بطريقة سرية. على هذا النحو ، يحظر أي الكشف.   وإلا فسيظل المسؤول الوحيد عن كيفية إستعماله للخدمات </br>
           
 أي حذف للحساب مهما كان دافعه يستلزم حذف كل المعلومات الشخصية الخاصة بالمستخدم. كل حدث صار بسبب قاهر وأدى إلى عطب في الموقع أو الخادم الحاسوبي و شريطة  أي انقطاع أو تغيير اثناء الصيانة لا تدخل في وصاية بسي-كو ولهذا يوافق المستخدم على عدم مساءلتنا في هذه الحالات المذكورة  </br>
 وفي إمكان المستخدم التواصل مع الموقع من خلال الرسائل الالكترونية إلى البريد الالكتروني للحرر المذكور في المادة الأولى </p></br>
    </article>
