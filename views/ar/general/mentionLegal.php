        <h2><strong> المادة 4</strong> :الملكية الفكرية</h2>
        <p>تخضع العلامات التجارية والشعارات واللافتات وجميع محتويات الموقع (النصوص والصور ...) للحماية بموجب قانون الملكية الفكرية وبشكل خاص بحقوق النشر.
            يحظر تمامًا أي تمثيل و / أو استنساخ و / أو استغلال جزئي أو كلي لهذه العلامة التجارية ، أيا كانت طبيعتها.
            يجب على المستخدم طلب التفويض المسبق للموقع لأي استنساخ أو نشر أو نسخ من المحتوى المختلف. يتعهد باستخدام محتويات الموقع في مكان خاص تمامًا ، ويحظر تمامًا أي استخدام للأغراض التجارية والإعلانية.
            أي تمثيل كلي أو جزئي لهذا الموقع من قبل أي عملية من أي نوع ، دون إذن صريح من مشغل الموقع يمثل انتهاكًا يعاقب عليه المادة L 335-2 وما يليها من قانون الملكية الفكرية.
            يُذكر أنه وفقًا للمادة L122-5 من قانون الملكية الفكرية ، يجب على المستخدم الذي يعيد إنتاج أو نسخ أو نشر المحتوى المحمي أن يذكر المؤلف ومصدره.
        </p></br>
    </article>
