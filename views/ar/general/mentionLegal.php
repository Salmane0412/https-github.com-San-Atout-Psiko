        <h1>الشروط العامة للإستخدام لموقع شركة بسي-كو</h1>
        <h2>اعتبارا من  19/03/2020</h2>
    </article>
    <article class="introduction">
        <p></br></br> المبتغى من الشروط العامة للإستخدام ( نقول ش.ع.إ) هو التأطير القانوني لطرائق إتاحة الموقع و الخدمات من قبل بسي-كو و تحديد شروط الولوج و إستعمال الخدمات من قبل "المستخدم" </br></br>

        كل تسجيلٍ في الموقع أو إستعماله يستلزم قبول هذه الشروط العامة للإستعمال دون تحفّظات أو قيود من قبل المستعمل </br>
        أثناء التسجيل على الموقع من خلال إستمارة التسجيل كل مستخدم يقبل بوجه التحديد الشروط العامة للإستخدام وذلك بالنقر على الخانة التي تسبق النص القادم ذكره: « أقر بأنني قرأت ووعيت "ش.ع.إ" وأقبلها  ». </br> </br>
       <br> في حالة عدم قبول ال: "ش.ع. " المنصوص عنها في هذا العقد يتوجب على المستخدم الاتقطاع على الولوج إلى الخدمات المقدمة من طرف الموقع. بسي-كو لها الحق الكامل في تعديل المحتوى القوانين في أي قت و بصورة انفرادية </p></br>
    </article>

