        <h2><strong>المادة 1</strong> :الاشعارات القانونية</h2>
        <p> تم تحرير الموقع تيابة عن شركة انفينيتي ميزور من قبل أعضاء الشركة الناشئة بسي-كو  ( كوم ليتيور, بول مولينير, أكيستان روسيت-رفيير , ماكسيميليان الصدقي, ميلفين سينيفاسن, سلمان والقدور) حيث أت المؤطر العام هو المعهد العالي للإلكتروتيك في باريس : مدرسة المهندسين للرقمية, شارع فانف, اسي-لي-مولينو.contact@infinitemeasures.fr   يمكنكم التواصل مع الشركة عبر الباريد الالكتروني  </p></br>
    </article>
