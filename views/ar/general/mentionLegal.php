        <h2><strong> المادة 5</strong> : المسؤولية</h2>
        <p>تعتبر مصادر المعلومات المنشورة على موقع Ψ-Co موثوقة ، لكن الموقع لا يضمن خلوه من العيوب أو الأخطاء أو السهو. يتم تقديم المعلومات المرسلة للمعلومات وللأغراض العامة دون قيمة تعاقدية.
            على الرغم من التحديثات المنتظمة ، لا يمكن تحميل موقع Ψ-Co مسؤولية تعديل الأحكام الإدارية والقانونية التي تحدث بعد النشر. وبالمثل ، لا يمكن تحميل الموقع مسؤولية استخدام وتفسير المعلومات الواردة في هذا الموقع.
            يتأكد المستخدم من الحفاظ على سرية كلمة المرور الخاصة به. يحظر أي إفشاء لكلمة المرور مهما كان نوعها  لتفادي أي خطر محتمل و لذا نحملك كل المسؤلية 
            لا يمكن اعتبار موقع Ψ-Co مسؤولاً عن أي فيروسات يمكن أن تصيب الكمبيوتر أو أي جهاز كمبيوتر لمستخدم الإنترنت ، بعد الاستخدام أو الوصول أو التنزيل من هذا الموقع.
           لا نتحمل المسؤولية في حالة وجود قوى خارجة عن السيطرة أو غير محتملة
        </p></br>
    </article>
