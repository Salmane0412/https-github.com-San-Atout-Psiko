    <p>
        Infinite Measures to firma, która zapewnia szkołom nauki jazdy i szkoleniom kierowców testy
        pozwalając ocenić zdolności psychotechniczne swoich uczniów. Za pomocą Ѱ-CO mamy
        dlatego opracował system testowania umiejętności psycho-technicznych u niektórych osób. </br>
        Na stronie Nieskończone miary możesz wybrać wyniki swoich testów, jeśli je zrobiłeś.
        Aby się tam dostać, po prostu zaloguj się na swoje konto lub utwórz je z danych kontaktowych
        dali podczas przejścia testu. Możesz również dokonać opisu różnych testów, które
        masz lub możesz móc z nami występować i wiele więcej. Aby uzyskać więcej informacji o
        strona, przesłuchanie się z nami, postaramy się odpowiedzieć tak szybko, jak to możliwe.

        
    </p>

</section>

<section id="Frequence_cardiaque">
    <article id="introduction">
        <h3>Tętno : </h3>
        <p>Ten test polega na pomiarze liczby uderzeń serca na jednostkę czasu oraz, 
            że każda zmiana tych pulsów w spoczynku lub podczas aktywności fizycznej może wskazywać 
            na istnienie pewnego poziomu stresu, ponieważ niektóre osoby mogą się bać  przed samolotem, 
            i próba wysiłkowa poprzez pomiar tętna informuje, czy pilot czuje się dobrze w swoim 
            otoczeniu (depresja barometryczna, nagłe wypadki samolotem).</p>
    </article>

</section>
<section id="Temperature_peau">
    <article id="introduction">
        <h3>Temperatura skóry:</h3>
        <p> Celem tego testu jest pomiar temperatury powierzchni skóry. Jest to metoda, 
            która pozwoli nam poznać stan fizyczny i psychiczny użytkownika, to znaczy: 
            zbyt wysoka temperatura może wskazywać na istnienie choroby lub bardzo wysoki
            poziom stresu . Aby przeprowadzić ten test, wystarczy na pewien czas położyć
            palec na czujniku temperatury, aby uzyskać dokładną wartość.</p>
    </article>

</section>
<section id="Reflexes_visuels">
    <article id="introduction">
        <h3>Odruchy wzrokowe:</h3>
        <p> Ten test polega na sprawdzeniu zdolności kierowcy (samolot, samochód); szybko reagować
            ze swoim otoczeniem i skoncentrować się na swojej pracy pomimo obecności zewnętrznych 
            zakłóceń. Aby to zrobić, użytkownik skoncentruje się na diodzie, a na jej dwóch 
            wymiarach umieścimy wielokolorowe diody LED, które zapalą się losowo, a jeśli jedna 
            z bocznych diod LED zaświeci się, naciśnij przycisk i tak dalej ; aby mieć wynik na końcu.</p>
    </article>

</section>
<section id="Reconnaissance_tonalite">
    <article id="introduction">
        <h3>Rozpoznawanie tonów:</h3>
        <p> Ten test mierzy zdolność kierowcy do odtworzenia dźwięku po jego wysłuchaniu.
            Aby zagwarantować całkowitą interakcję ze środowiskiem. Aby to zrobić, użytkownik 
            słucha słuchawek, które generują dźwięk, a następnie użytkownik będzie próbował 
            odtworzyć dźwięk, śpiewając w pobliżu mikrofonu.</p>
    </article>

</section>
<section id="Memorisation_couleurs">
    <article id="introduction">
        <h3>Zapamiętywanie kolorów: </h3>
        <p> Aby przetestować zdolność kierowcy do zapamiętania różnych wydarzeń wokół niego
            i zapamiętania kilku zadań podczas jazdy. Aby wykonać ten test, użytkownik spróbuje
            skoncentrować się na kolorowej serii diod LED, które zapalają się losowo, a następnie
            nacisnąć przyciski odpowiadające poszczególnym kolorom, a jeśli będzie mógł zrobić 
            to samo ponownie, przydzielimy mu wynik testu.</p>
    </article>

</section>